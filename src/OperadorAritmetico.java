public class OperadorAritmetico {
    public static void main(String[] args) {
        int i = 5, j = 4;
        int suma = i + j;
        System.out.println("suma = " + suma);
        System.out.println("i + j = " + i + j);
        double div = (float)i/j;
        System.out.println("div = " + div);
    }
}
